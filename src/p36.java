public class p36 {
    public static void main(String[] args) {
        String str = "Hello";
        char ch1 = str.charAt(0);
        char ch2 = str.charAt(1);

        int len = str.length();

        System.out.println("The first char in " + str + " is " + ch1);
        System.out.println("The second char in " + str + " is " + ch2);
        System.out.println("The length of " + str + " is " + len);
    }
}
